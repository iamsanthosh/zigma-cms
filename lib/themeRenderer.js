export async function getThemeData(themeId = 1) {
  try {
    const theme = await query(
      `SELECT * FROM themes WHERE id = ?`,
      [themeId]
    );

    const settings = await query(
      `SELECT * FROM theme_settings WHERE theme_id = ?`,
      [themeId]
    );

    const typography = await query(
      `SELECT * FROM theme_typography WHERE theme_id = ?`,
      [themeId]
    );

    const breakpoints = await query(
      `SELECT * FROM theme_breakpoints WHERE theme_id = ?`,
      [themeId]
    );

    return {
      theme: theme[0],
      settings,
      typography,
      breakpoints
    };
  } catch (error) {
    console.error('Error fetching theme data:', error);
    return null;
  }
}

export async function getActiveThemeId() {
  try {
    const result = await query(
      `SELECT id FROM themes WHERE is_active = 1 AND is_default = 1 LIMIT 1`
    );
    return result[0]?.id || 1;
  } catch (error) {
    console.error('Error fetching active theme ID:', error);
    return 1;
  }
}
